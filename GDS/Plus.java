public class Plus {
    public static void main(String[] args) {
	// method (메소드) call 호출
    	System.out.println(10 + "10");
	System.out.println("20" + 20);
	System.out.println(); // println 한 줄 씩 출력 확인 가능
	System.out.println(5.123);
	System.out.println(0.1 + 0.1);

	System.out.print(1); // print : 한 줄 띄움 없이 붙어서 출력
	System.out.print(2);
	System.out.print(3);
    }
}