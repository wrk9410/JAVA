// 관계연산자

public class RelOper {
	public static void main(String[] args){
		System.out.println(55 == 55); // 같은 수인지
		System.out.println(55 != 55); // 같지 않은지
		System.out.println(55 > 55); // > 왼쪽이 오른쪽보다 큰 지
		System.out.println(55 < 55); // < 오른쪽이 왼쪽보다 큰 지
		System.out.println(55 >= 55); // 왼쪽이 오른쪽보다 크거나 같은지
		System.out.println(55 <= 55); // 오른쪽이 왼쪽보다 크거나 같은지
	}
}